#  /" - escape double quote; allows you to include a " without ending it.
print("Hi \"Python\" ")

#  \\ - escape backslash; use real backslash \ not as start of an escape sequence
print("Path: C:\\Users\\Mehruza")

#  empty print simply prints a blank(new line)
print("Message1")
print()
print("Message2")

#  \n - escpe new line; adds a new line, it moves the text that comes after it to the next line
print("Message1\n")  # \n\n - two new lines and so on
print("Message2")
print("Message1\nMessage2")

#  \t - escape tab; adds a tab space
print("Message1\tMessage2")


