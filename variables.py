#  What is a Variable?  A: A variable is a name you create to store a value, so you can use it later in your code
print("My name is Mehruza")
print("Mehruza is learning Python")
print("Mehruza wants to become Python expert")

name = "Maria"
print("My name is", name)
print(name, "is learning Python")
print(name, "wants to become Python expert")

name = "Charlotte"
print("My name is", name)
print(name, "is learning Python")
print(name, "wants to become Python expert")

name = "Mehruza"
language = "JavaScript"
