#  INPUT
#  print() - display something to users
#  input() - get something from users

#  input() and variables
#  using input() alone reads the user's response but immediately discards it. to keep the value, assign it to a variable.
name = input("Enter Your Name:")
print("You are", name)

#  Hard-coded(static) value - fixed piece of data written directly into your code that never changes at runtime.
#  Dynamic values - data entered by the user that can vary each time the program runs.
name = input("Enter Your Name:")
country = "Germany"
print(name, "comes from", country)

#  input() makes your programm interactive; input lets your program ask questions and react to what the user types, making it feel alive.
